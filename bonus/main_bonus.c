int	main(int ac, char **av)
{
	t_data	*data;

	if (ac != 2)
		return (write(2, "Error. Wrong number of arguments\n", 34), 1);
	data = init_cub(av[1]);
	if (!data)
		return (1);
	mlx_loop(data->mlx);
	mlx_do_sync(data->mlx);
	free_cub(data);
	return (0);
}
